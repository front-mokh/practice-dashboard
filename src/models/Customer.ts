import mongoose from "mongoose";
const Schema = mongoose.Schema;
const CustomerSchema = new Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
});

export default mongoose.models.Customer || mongoose.model("Customer", CustomerSchema);
